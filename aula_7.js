var cartaMaquina;
var cartaJogador;

function sortearCarta(){

	var numeroCartaMaquina = parseInt(Math.random() * 43);
	cartaMaquina = cartas[numeroCartaMaquina];

	var numeroCartaJogador = parseInt(Math.random() * 43);

	while(numeroCartaMaquina == numeroCartaJogador){
		numeroCartaJogador = parseInt(Math.random() * 43);
	}

	cartaJogador = cartas[numeroCartaJogador];

	document.getElementById('btnSortear').disabled = true;
	document.getElementById('btnJogar').disabled = false;

	exibirCartaJogador();
}
function obtemAtributoSelecionado(){

	var radioAtributos = document.getElementsByName("atributos")

	for(var i = 0; i < radioAtributos.length; i++){
		if(radioAtributos[i].checked == true){
			return radioAtributos[i].value
		}
	}
}
function jogar(){
	var atributoSelecionado = obtemAtributoSelecionado();
	var elementoResultado = document.getElementById("resultado");

	var valorCartaJogador = (cartaJogador.atributos[atributoSelecionado]);
	var valorCartaMaquina = (cartaMaquina.atributos[atributoSelecionado]);

	if (valorCartaJogador > valorCartaMaquina){
		elementoResultado.innerHTML = "YOU WIN!"
	} else if (valorCartaJogador < valorCartaMaquina){
		elementoResultado.innerHTML = "YOU LOOSE :("
	} else {
		elementoResultado.innerHTML = "We have a tie..."
	};

	exibirCartaMaquina();
}
function exibirCartaJogador(){
	var divCartaJogador = document.getElementById("carta-jogador");
	divCartaJogador.style.backgroundImage=`url(${cartaJogador.imagem})`;

	var moldura = '<img src="https://www.alura.com.br/assets/img/imersoes/dev-2021/card-super-trunfo-transparent-ajustado.png" style=" width: inherit; height: inherit; position: absolute;">';
	var tagHTML = "<div id='opcoes' class='carta-status'>";

	var opcoesTexto = "";
	for(var atributos in cartaJogador.atributos){
		opcoesTexto += "<input type='radio' name='atributos' required value='" +atributos + "'>" + atributos + " " + cartaJogador.atributos[atributos] + "<br>";
	}

	var nome = `<p class="carta-subtitle">${cartaJogador.nome}</p>"`;

	divCartaJogador.innerHTML = moldura + nome + tagHTML + opcoesTexto + "</div>";
}
